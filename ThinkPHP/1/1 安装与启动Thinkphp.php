	一、安装thinkPHP5.1

	1、环境要求

		PHP >= 5.6.0
		
		PDO PHP Extension
		
		MBstring PHP Extension


	2、安装

		composer create-project --prefer-dist topthink/think=5.1.* tp5 

			composer 			表示执行composer程序

			create-project 		通过composer去创建项目

			topthink/think  	框架的名称

			--prefer-dist  		优先下载压缩包方式，而不是直接从github上下载源码